export const UNLOCKABLE_WORDS_BY_ID = Object.freeze(
  UNLOCKABLE_WORDS.reduce((acc, entry) => {
    acc[entry.id] = entry;
    return acc;
  }, {})
);

export function getUnlockableWordById(wordId) {
  return UNLOCKABLE_WORDS_BY_ID[wordId] || null;
}

export function pickRandomLockedWordId(unlockedWordIds = [], randomValue = Math.random) {
  const locked = UNLOCKABLE_WORDS.filter((entry) => !unlockedWordIds.includes(entry.id));
  if (!locked.length) return null;
  const index = Math.max(0, Math.min(locked.length - 1, Math.floor(randomValue() * locked.length)));
  return locked[index].id;
}
