const KEY_TO_LANG = {
  es: 'español',
  en: 'inglés',
  fr: 'francés',
  it: 'italiano',
  de: 'alemán',
  pt: 'portugués',
};

export function getUILang() {
  return localStorage.getItem('hp_ui_lang') || 'es';
}

export function setUILang(lang) {
  localStorage.setItem('hp_ui_lang', lang);
}

export function t(key, lang) {
  const l = lang || getUILang();
  const str = translations[l]?.[key];
  if (str === undefined) return translations.es[key] || key;
  return str;
}

export { translations, LANG_TO_KEY, KEY_TO_LANG };
